class Calculator
    """Performs the four basic mathematical operations

    Methods:
    add(number, number)
    subtract(number, number)
    multiply(number, number)
    divide(number,number)
    """

    def add(firstOperand, secondOperand)
        """Adds two numbers together

        Arguments:
        firstOperand - Any number
        secondOperand - An number
        """

        return firstOperand + secondOperand
    end

    def subtract(firstOperand, secondOperand)
        """Adds two numbers together

        Arguments:
        firstOperand - Any number
        secondOperand - An number
        """

        return firstOperand - secondOperand
    end

    def multiply(firstOperand, secondOperand)
        """Adds two numbers together

        Arguments:
        firstOperand - Any number
        secondOperand - An number
        """

        return firstOperand * secondOperand
    end
    def divide(firstOperand, secondOperand)
        """Adds two numbers together

        Arguments:
        firstOperand - Any number
        secondOperand - An number
        """

        return firstOperand / secondOperand
    end
end